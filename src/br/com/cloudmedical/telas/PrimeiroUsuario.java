    public void cadastrar(){
        UsuarioClasse user = new UsuarioClasse();
//        Configuracao config = new Configuracao();
        
        int dia = cal.get(Calendar.DAY_OF_MONTH);
        int mes = cal.get(Calendar.MONTH) + 1;
        int ano = cal.get(Calendar.YEAR);
        String mesLongo = null;
        
        if(mes < 10){
            mesLongo = "0" + mes;
        }else{
            mesLongo = String.valueOf(mes);
        }
        
        String dataHoje = ano + "-" + mesLongo + "-" + dia;
        
        user.setNomeUsuario(txtLogin.getText());
        user.setSenha(String.valueOf(txtSenha.getPassword()));
        user.setEmail(txtEmail.getText());
        user.setDataCadastro(dataHoje);
        user.setDataAlteracao(dataHoje);
        
//        config.criaXML(txtLogin.getText(), txtEmail.getText());
        
        user.insereUsuario();
        
        sair();
//        System.out.println(user.getDataCadastro());
//        System.out.println(user.getDataAlteracao());
        
    }
    
